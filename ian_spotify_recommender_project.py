            try: # using try-except here so no ugly err warnings lol
                print(f"\nSearching based on '{seed_song}'...")
                recommendations = recommender_by_name(seed_song, 10)
                better_looking_results(recommendations)
            except ValueError as Bruh:
                print(f"\nError: {Bruh}")
