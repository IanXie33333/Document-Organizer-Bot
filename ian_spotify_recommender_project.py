# Recommender:
# A function with inputs:
# seed_id, k

# Search by ID
def recommender_by_seed_id(seed_id, k):
  index = trackid_to_index[seed_id]
  distances, indices = knn.kneighbors(X_scaled[index : index + 1], n_neighbors = min(k+1, len(df_songs)))
  recommended_indecies = [i for i in indices[0] if i != index][:k]
  return df_songs.iloc[recommended_indecies][[Track_id, Song_name, 'genre']]

# Search by name
def recommender_by_name(seed_name, k):
  if Song_name not in df_songs.columns:
    raise ValueError("No song name on record")


  the_song = df_songs[df_songs[Song_name].str.lower() == str(seed_name).lower()]

  if the_song.empty:
    raise ValueError("Seed song not found.")
  seed_id = the_song.iloc[0][Track_id]

  return recommender_by_seed_id(seed_id, k)

# Search by Feature
def recommender_by_features(user_features_dict, k=10):
# Takes user input, fills missing features with dataset averages,
# scales the data, and finds nearest songs."

    input_vector = df_songs[feature_cols].mean() # Create a vector with the
                                                  # average values of the dataset

    for feature, value in user_features_dict.items(): # Update with
                                                      # user's specific inputs
        if feature in feature_cols:
            input_vector[feature] = value

    input_df = pd.DataFrame([input_vector], columns=feature_cols) # Convert to dataframe for the scaler

    input_scaled = scaler.transform(input_df) # Convert to dataframe for the scaler

    distances, indices = knn.kneighbors(input_scaled, n_neighbors=k) # Find neighbors

    return df_songs.iloc[indices[0]][[Track_id, Song_name, 'genre']] # results

def get_valid_input(prompt): # user can only enter floats in designed range
    """Ensures user enters a float between 0.0 and 1.0"""
    while True:
        try:
            value = float(input(prompt))
            if 0.0 <= value <= 1.0:
                return value
            print("  -> Please enter a number between 0.0 and 1.0.")
        except ValueError:
            print("  -> Invalid input. Please enter a number.")

def better_looking_results(recommendations_df): # print out result better
    print("\n" + "-"*50)
    print(f"{'#':<4} {'Song Name':<30} {'Genre':<15}")
    print("-"*50)

    counter = 1

    for index, row in recommendations_df.iterrows():
        name = row[Song_name]
        genre = row['genre']

        display_name = (name[:27] + '..') if len(name) > 27 else name

        print(f"{counter:<4} {display_name:<30} {genre:<15} ")
